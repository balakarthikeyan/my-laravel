    {
        Post::create($request->all());

        $subscribers = \App\User::all(); //Retrieving all subscribers
 
        foreach($subscribers as $subscriber){
            Notification::route('mail' , $subscriber->email)->notify(new \App\Notifications\NewPostNotification($request->all())); //With new post
        }

    }

